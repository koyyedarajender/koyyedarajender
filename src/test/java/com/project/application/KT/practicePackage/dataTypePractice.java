package com.project.application.KT.practicePackage;

public class dataTypePractice {


    public static void main(String[] args) {
        int data = 2;
        String name = "Raji";
        float floaddata = 3;
        double doubledata = 2;
        boolean booleanData = true;
        boolean booleanFalseData = false;
        System.out.println("int  ::: -> " + data);
        System.out.println("name  ::: -> " + name);
        System.out.println("double  ::: -> " + doubledata);
        System.out.println("float   ::: -> " + floaddata);
        System.out.println("boolean true ::: -> " + booleanData);
        System.out.println("boolean false ::: -> " + booleanFalseData);

    }


}
